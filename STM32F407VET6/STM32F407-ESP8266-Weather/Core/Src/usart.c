/**
 * @brief   Receives data from UART.
 * @param   *data: Array to save the received data.
 * @param   length:  Size of the data.
 * @return  HAL status.
 */
HAL_StatusTypeDef uart_receive(UART_HandleTypeDef huart, uint8_t *data, uint16_t length)
{
	return HAL_UART_Receive(&huart, data, length, UART_TIMEOUT);
}

/**
 * @brief   Transmits a string to UART.
 * @param   *data: Array of the data.
 * @return  status: Report about the success of the transmission.
 */
HAL_StatusTypeDef uart_transmit_str(UART_HandleTypeDef huart, uint8_t *data)
{
	uint16_t length = 0u;

	/* Calculate the length. */
	while ('\0' != data[length])
	{
		length++;
	}
	return HAL_UART_Transmit(&huart, data, length, UART_TIMEOUT);
}

/**
 * @brief   Transmits a single char to UART.
 * @param   *data: The char.
 * @return  status: Report about the success of the transmission.
 */
HAL_StatusTypeDef uart_transmit_ch(UART_HandleTypeDef huart, uint8_t data)
{
	if (huart.gState == HAL_UART_STATE_TIMEOUT)
	{
		huart.gState = HAL_UART_STATE_READY;
	}
	return HAL_UART_Transmit(&huart, &data, 1u, UART_TIMEOUT);
}

